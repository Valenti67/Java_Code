public class PrintDemo {
    public static void main(String[] args) {
        System.out.println("Hello");
        System.out.print("Hello");
        System.out.printf("%s %d \n", "hello", 10);

        
        System.out.format("%s %d \n", "hello", 10);
        System.out.println("hello" + " " + 10 + " " + "\n");
    }
}
